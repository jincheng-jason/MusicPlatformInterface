package com.ysten.MusicPlatformInterface.domain;

public class Singer {

	private String singerCode;
	
	private String initials;
	
	private String singerName;
	
	private String introduction;
	
	private String singerPicture;
	
	private String property;
	
	private int operateType;

	

	public String getSingerCode() {
		return singerCode;
	}

	public void setSingerCode(String singerCode) {
		this.singerCode = singerCode;
	}

	public String getInitials() {
		return initials;
	}

	public void setInitials(String initials) {
		this.initials = initials;
	}

	public String getSingerName() {
		return singerName;
	}

	public void setSingerName(String singerName) {
		this.singerName = singerName;
	}

	public String getIntroduction() {
		return introduction;
	}

	public void setIntroduction(String introduction) {
		this.introduction = introduction;
	}

	public String getSingerPicture() {
		return singerPicture;
	}

	public void setSingerPicture(String singerPicture) {
		this.singerPicture = singerPicture;
	}

	public String getProperty() {
		return property;
	}

	public void setProperty(String property) {
		this.property = property;
	}

	public int getOperateType() {
		return operateType;
	}

	public void setOperateType(int operateType) {
		this.operateType = operateType;
	}
	
	
}
